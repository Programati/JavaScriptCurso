/**
 * APIs del navegador
 * Los navegadores nos dan acceso a diferentes API con las que podemos:
 * - Local Storage API - Trabaja con almacenamiento local
 * - Fetch API - Hace peticiones a servidores
 * - History API - Para poder acceder al historial de la página
 * - Validation API - Para hacer uso de las funciones del navegador para validar formularios
 * - Más
 */
/**
 * APIs Externas
 * También puede haber APIs externas que nos permiten acceder a recursos de otra página web.
 * Ejemplo con la API de youtube:
 * - Nos permite obtener información de videos y canales
 * - Nos permite subir videos
 * 
 * Estas son cosas que no podriamos hacer desde nuestra página si no existira una API.
 * Gracias a que existe una API, podemos hablarle y pedirle obtener datos y hacer operaciones
 */